export async function getStaticProps() {
    const allPostsData = getSortedPostsData();
    const pokemons = await fetch('https://pokeapi.co/api/v2/pokemon?limit=10&offset=0')
        .then(response => response.json())
        .then(data => data.results);
    return {
        props: {
            allPostsData,
            pokemons
        },
    };
}